package model;

public class Artista {
	
	private int artistaID;
	private String nome;
	
	
	public int getArtistaID() {
		return artistaID;
	}
	
	public void setArtistaID(int artistaID) {
		this.artistaID = artistaID;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
